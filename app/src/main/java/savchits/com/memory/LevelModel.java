package savchits.com.memory;

public class LevelModel {
    String level;
    int photo;

    public LevelModel(String level , int photo) {
        this.level = level;
        this.photo = photo;
    }

    public
    String getLevel() {
        return level;
    }

    public
    String setLevel() {
        return level;
    }

    public
    int getPhoto() {
        return photo;
    }
    public
    int setPhoto() {
        return photo;
    }
}
